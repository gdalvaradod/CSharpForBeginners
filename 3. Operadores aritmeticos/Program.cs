/*
Operadores aritméticos

(+) Suma
(-) Resta
(*) Multiplicación
(/) División
(%) Residual
*/

// Declaración de variables
int num1 = 5;
int num2 = 15;
int resultado = 0;

// Suma
resultado = num1 + num2;
Console.WriteLine("Suma: " + resultado);

// Resta
resultado = num2 - num1;
Console.WriteLine("Resta: " + resultado);

// Multiplicación
resultado = num1 * num2;
Console.WriteLine("Multiplicación: " + resultado);

// División
resultado = num2 / num1;
Console.WriteLine("División: " + resultado);

// Residual
resultado = num2 % num1;
Console.WriteLine("Residual: " + resultado);

