/*
For

for(variable de referencia; condición a cumplir; incremento de variable de referencia)
*/
for(int i = 0; i < 5; i++){
    Console.Write(i + 1);
}

/*
While

while(condición){}
*/
int a = 0;

while(a < 5){
    Console.WriteLine(a + 1);
    a++;
}

